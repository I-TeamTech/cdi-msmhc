		                    				<h4><a href="#">Career</a></h4>
		                    				<span class="line-seperator"></span>
		                    				<p>
<p style="text-align: justify;">
	We invite you to explore the many opportunities and benefits of joining CDI.</p>
<p style="text-align: justify;">
	CDI is a social wing, who is committed to empower the marginalised communities of the society, especially women and children.  Thus, we are “Committed to a civilization”, where people live in unity based on values of justice and love.</p>
<p style="text-align: justify;">
	We offer professionals a unique opportunity to contribute to help transforms broken lives and bring sustainable changes in various way. We are determined and committed to empower the poor and marginalised women, girls and children; prepare and equip competent and committed leaders for communities’ next generation; install in all, the spirit of motivation for empowerment; transformation of the young as agents of change through employable, educative and accompaniment process; facilitate resources for development work in the Provinces, and help the sisters, staff and collaborators to become capable and effective in organizing women, girls and children.  </p>
<p style="text-align: justify;">
	Indeed, CDI takes pride in going the extra mile to serve people who are in need. Our staff use their skills to find innovative, long-lasting solutions that better support the people we serve. Therefore, we work with skilled professionals who are committed to put people first, make a real difference in people’s lives and ensure their basic safety, rights and dignity.</p>
<p style="text-align: justify;">
	Interested in becoming a champion for the vulnerable group of people who are in need of help and wish to be a part of the change process at the grassroots level, then CDI is for you.</p>
<p style="text-align: justify;">
	Moreover, if you are willing to build a career in the development sector through professional value creation, come, “Aim High & Fly High” with CDI,</p>
